export function getInitials(name: string): string {
  return name
    .split(' ')
    .map((n) => n[0])
    .join('')
    .toUpperCase()
    .slice(0, 2);
}

export function generateEmployeeId(
  companyName: string = "Dayflow",
  fullName: string = "John Doe",
  year: number = new Date().getFullYear(),
  serial: number = 1
): string {
  const companyPrefix = companyName.substring(0, 2).toUpperCase();
  const nameParts = fullName.split(' ');
  const firstName = nameParts[0] || "";
  const lastName = nameParts[1] || firstName;
  
  const firstNamePrefix = firstName.substring(0, 2).toUpperCase().padEnd(2, 'X');
  const lastNamePrefix = lastName.substring(0, 2).toUpperCase().padEnd(2, 'X');
  
  const yearString = year.toString();
  const serialString = serial.toString().padStart(4, '0');
  
  return `${companyPrefix}${firstNamePrefix}${lastNamePrefix}${yearString}${serialString}`;
}
